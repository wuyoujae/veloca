export type I18nKey = keyof typeof translations.en;
export type Translator = (key: I18nKey, params?: TranslationParams) => string;

export function resolveLanguage(language: AppLanguage): ResolvedLanguage {
  if (language !== 'system') {
    return language;
  }

  if (typeof navigator === 'undefined' || !navigator.language) {
    return 'en';
  }

  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

export function translate(language: ResolvedLanguage, key: I18nKey, params: TranslationParams = {}): string {
  const template = translations[language][key] ?? translations.en[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (match, paramKey: string) => {
    const value = params[paramKey];
    return value === undefined ? match : String(value);
  });
}

export function createTranslator(language: ResolvedLanguage): Translator {
  return (key, params) => translate(language, key, params);
}
